public class StringSwitch{
	
	public static void main(String args[]){
		
		String str = "two";
		
			switch(str){
				case "one":
				System.out.println("str is one");
				//break;
				
				case "two":
				System.out.println("str is two");
				//break;
				
				case "three":
				System.out.println("str is three");
				break;
				
				default:
				System.out.println("str is something else");
							
			}
	}
}
